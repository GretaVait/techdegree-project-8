class Employee {
    constructor (firstName, lastName, email, city, image, phoneNumber, address, state, postcode, birthday) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.city = city;
        this.image = image;
        this.phoneNumber = phoneNumber;
        this.address = address;
        this.state = state;
        this.postcode = postcode;
        this.birthday = birthday;
    }
}